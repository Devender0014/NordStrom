let cartLs=  JSON.parse(localStorage.getItem("cart_page")) || [];


function handlesalrysort(){
    
      let selected=document.querySelector("#sortsalary").value

      if(selected=="Low to High"){
         homedata.sort(function(a,b){
           return a.price-b.price;
         })
         display(homedata);
      }
      if(selected=="High to low"){
         homedata.sort(function(a,b){
           return b.price-a.price
         })
         display(homedata);
      }
    }
          


       function display(homedata){
        document.querySelector("#container").innerHTML="";
        homedata.forEach(function(elem){
            
    let box=document.createElement("div")
    let image=document.createElement("img")
    image.setAttribute("src",elem.image_url)
    let name=document.createElement("h4")
    name.innerText=elem.name;
    let price=document.createElement("p")
    price.innerText=elem.price;
    let button=document.createElement("button")
    button.innerText="Add to cart"
    button.addEventListener("click",function(){
        if(addtocart(elem.productid)==true){
            alert("Product added to Suessfully")
            cartLs.push(elem);
            localStorage.setItem("cart_page",JSON.stringify(cartLs));
        }
        else{
            alert("Product allready in the cart")
        }
         addtocart(elem.productid);
    })


    box.append(image,name,price,button)
    document.querySelector("#container").append(box)
  })
       }
       
     display(homedata);


    function addtocart(id){
     for(let i=0;i<cartLs.length;i++){
     if(cartLs[i].productid==id){
        return false;
     }
}
return true;
       }


