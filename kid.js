function show(productData){

  let main=document.querySelector("#display")
    main.innerHTML="";
    productData.forEach(function(elm){
      

        let dis=document.querySelector("#display")
        let div=document.createElement("div")
      
        let img=document.createElement("img")
        img.setAttribute("src",elm. image_url)
      
        let Bname=document.createElement("h2")
        Bname.innerText=elm.brand
      
        let Pname=document.createElement("h3")
        Pname.innerText=elm.name
      
        let price=document.createElement("h3")
price.innerText=elm.price
        
      
        let btn=document.createElement("button")
        btn.innerText="Add toCart"
        btn.addEventListener("click",function(){
          addToCartFun(elm)
        })
      
      
        dis.append(div)
        div.append(img,Bname,Pname,price,btn)
      
      })
      let cartLS=[];
      function addToCartFun(elm){
        cartLS.push(elm)
        localStorage.setItem("cart_page",JSON.stringify(cartLS))
        alert("Product added to your Cart")
      
      }
}
